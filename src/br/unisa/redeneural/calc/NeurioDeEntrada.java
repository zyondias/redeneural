package br.unisa.redeneural.calc;

/**
 * 
 * Class para mapear o neuronio de entrada
 * 
 * @author zyon.silva
 *
 */
public class NeurioDeEntrada {
	private int entradaUm;
	private int entradaDois;
	private int entradaTres;
	private int entradaQuatro;

	/**
	 * @return int
	 */
	public int getEntradaUm() {
		return entradaUm;
	}

	/**
	 * @param entradaUm
	 */
	public void setEntradaUm(int entradaUm) {
		this.entradaUm = entradaUm;
	}

	/**
	 * @return int
	 */
	public int getEntradaDois() {
		return entradaDois;
	}

	/**
	 * @param entradaDois
	 */
	public void setEntradaDois(int entradaDois) {
		this.entradaDois = entradaDois;
	}

	/**
	 * @return int
	 */
	public int getEntradaTres() {
		return entradaTres;
	}

	/**
	 * @param entradaTres
	 */
	public void setEntradaTres(int entradaTres) {
		this.entradaTres = entradaTres;
	}

	/**
	 * @return int
	 */
	public int getEntradaQuatro() {
		return entradaQuatro;
	}

	/**
	 * @param entradaQuatro
	 */
	public void setEntradaQuatro(int entradaQuatro) {
		this.entradaQuatro = entradaQuatro;
	}

}
